def take_fft(t, data):
    dt = (t[-1]-t[0])/len(t)
    sample_rate = 1.0/dt
    print(sample_rate)

    y = data
    length = len(y)
    k = np.arange(length)
    T = length/sample_rate
    freq = k/T

    freq = freq[range(int(length/2))]
    Y = np.fft.fft(y)/length
    Y = Y[range(int(length/2))]

    fig, (ax1, ax2) = plt.subplots(2, 1)
    ax1.plot(t,y,'b')
    ax1.set_xlabel('Time')
    ax1.set_ylabel('Amplitude')
    ax2.loglog(freq,abs(Y),'b') # plotting the fft
    ax2.set_xlabel('Freq (Hz)')
    ax2.set_ylabel('|Y(freq)|')
    plt.show()


def comp_fft(t1, d1, t2, d2):
    dt1 = (t1[-1]-t1[0])/len(t1)
    sample_rate1 = 1.0/dt1

    y1 = d1
    length1 = len(y1)
    k1= np.arange(length1)
    T1 = length1/sample_rate1
    freq1 = k1/T1

    freq1 = freq1[range(int(length1/2))]
    Y1 = np.fft.fft(y1)/length1
    Y1 = Y1[range(int(length1/2))]

    dt2 = (t2[-1]-t2[0])/len(t2)
    sample_rate2 = 1.0/dt2

    y2 = d2
    length2 = len(y2)
