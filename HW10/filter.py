def maf(mafnum, t, d):
    mafA = []
    mafTA = []
    avg = 0
    for i in range(mafnum, len(d)):
        avg = sum(d[i-mafnum: i])/mafnum
        mafA.append(avg)
        mafTA.append(t[i])
    comp_fft(t, d, mafTA, mafA)



def iir(A, B, t, d):
    iirA = []
    iirTA = []
    avg = 0
    for i in range(1, len(t)):
        iirTA.append(t[i])
        avg = avg*A + d[i]*B
        iirA.append(avg)

    comp_fft(t,d,iirTA,iirA)


def fir(h, t, d):
    firA =[]
    firTA = []
    X = len(h)
    for i in range(X, len(d)):
        firTA.append(t[i])
        avg = 0
        for j in range(0, X):
            avg += d[i-j]*h[j]
        
        firA.append(avg)
    comp_fft(t,d, firTA, firA)





